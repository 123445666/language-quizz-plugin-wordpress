function stripAccents($str)
{
    $str = str_replace(' ', '-', $str);
    $str = str_replace(array("#", "'", ";", "", "(", ")", ".", ","), '', $str);
    return strtr(utf8_decode($str), utf8_decode('àáâãäçèéêëìíîïñòóôõöùúûüýÿÀÁÂÃÄÇÈÉÊËÌÍÎÏÑÒÓÔÕÖÙÚÛÜÝ'), 'aaaaaceeeeiiiinooooouuuuyyAAAAACEEEEIIIINOOOOOUUUUY');
}
